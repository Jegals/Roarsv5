    private void saveInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveInfoActionPerformed
        // TODO add your handling code here:

        String roomNumber = roomnumber.getText();
        String roomtype   = roomType.getText();
        String bed        = bedType.getText();
        String maxocc     = max.getText();
        String pricingg   = price.getText();
        String status     = statustxt.getText();

        if(roomNumber.isEmpty() ||  roomtype.isEmpty() ||  bed.isEmpty() ||  maxocc.isEmpty()
            ||  pricingg.isEmpty()  || status.isEmpty()){
            JOptionPane.showMessageDialog(null,"FILL IN ALL INFORMATION");
            return ;
        }
        
        if(roomNumberTaken(roomNumber)){// method
              JOptionPane.showMessageDialog(this,"Room Number Already Exist");//callling the method that do not take an info is the roomnum is dup
            return;
        }
    

        try (FileWriter writer = new FileWriter("data/rooms.txt", true)) {

            writer.write(roomNumber + "," + roomtype + "," + bed + "," +
                         maxocc + "," + pricingg + "," + status);          

            writer.write(System.lineSeparator());
            writer.flush();// to ensure na ma lagay agad yung info sa info after input

            if (mainPanel != null){
                mainPanel.loadTableData();

            }

            JOptionPane.showMessageDialog(null, "Successfully Saved");
            //    if (mainFrame != null) {
                mainPanel.loadTableData();
                javax.swing.SwingUtilities.getWindowAncestor(this).dispose();
                //   }
            //   new InsertData().setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error saving the file: " + e.getMessage());
        }

    }//GEN-LAST:event_saveInfoActionPerformed

